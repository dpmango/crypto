    function getPointCount() {
        if(window.innerWidth < 400) {
            sliceCharts(30);
        } else if (window.innerWidth < 767) {
            sliceCharts(50);
        } else {
            sliceCharts('all');
        }
    }

    function sliceCharts(pointsLength) {
        var key = $('.tabs_chart_content .active > div').attr('id');
        key = key.replace('#','');
        var btn = $('.active .add_point');

        switch (pointsLength) {
            case "all":
                charts[key].chart.options.data[0].dataPoints = charts[key].data[0].dataPoints;
                charts[key].chart.options.data[1].dataPoints = charts[key].data[1].dataPoints;
                charts[key].chart.render();

                btn.css('display', 'none');
                break;
            default:
                charts[key].chart.options.data[0].dataPoints = charts[key].data[0].dataPoints.slice(0, pointsLength);
                charts[key].chart.options.data[1].dataPoints = charts[key].data[1].dataPoints.slice(0, pointsLength);
                charts[key].chart.render();

                btn.css('display', 'block');
        }
    }

    function setFontSize() {
        if (window.innerWidth < 767) {
            for (var key in charts) {
                charts[key].chart.options.axisY2.labelFontSize = 12;
                charts[key].chart.options.axisX.labelFontSize = 12;
                charts[key].chart.render();
            }
        } else {
            for (var key in charts) {
                charts[key].chart.options.axisY2.labelFontSize = 15;
                charts[key].chart.options.axisX.labelFontSize = 15;
                charts[key].chart.render();
            }
        }
    }
